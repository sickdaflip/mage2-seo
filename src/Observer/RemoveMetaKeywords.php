declare(strict_types=1);

namespace FlipDev\Seo\Observer;

use FlipDev\Seo\Helper\Data as SeoHelper;
use Magento\Framework\Event\Observer;
use Magento\Framework\Event\ObserverInterface;
use Magento\Framework\View\Page\Config as PageConfig;
use Psr\Log\LoggerInterface;

/**
 * Remove meta keywords tags from page
 */
class RemoveMetaKeywords implements ObserverInterface
{
    private SeoHelper $seoHelper;
    private PageConfig $pageConfig;
    private LoggerInterface $logger;

    /**
     * Constructor
     *
     * @param SeoHelper $seoHelper
     * @param PageConfig $pageConfig
     * @param LoggerInterface $logger
     */
    public function __construct(
        SeoHelper $seoHelper,
        PageConfig $pageConfig,
        LoggerInterface $logger
    ) {
        $this->seoHelper = $seoHelper;
        $this->pageConfig = $pageConfig;
        $this->logger = $logger;
    }

    /**
     * Execute observer logic
     *
     * @param Observer $observer
     * @return void
     */
    public function execute(Observer $observer): void
    {
        try {
            if (!$this->seoHelper->getConfig('flipdev_seo/settings/removekeywords')) {
                return;
            }

            $this->pageConfig->setKeywords('');
            
            $this->logger->debug('FlipDev_Seo: Removed meta keywords');
        } catch (\Exception $e) {
            $this->logger->error('FlipDev_Seo: Remove keywords failed', [
                'exception' => $e->getMessage()
            ]);
        }
    }
}
